numero1 = 12
numero2 = 10

# > é um operador relacional para verificar MAIORIDADE
if (numero1 == numero2):
    print(f"Os número são iguais {numero1}")
else :
    if (numero1 > numero2):
        print(f"O maior número é {numero1}")
    else:
        print(f"O maior número é o {numero2}")
