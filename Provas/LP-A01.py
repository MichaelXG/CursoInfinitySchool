# Crie um exemplo de cada uma das principais tipagens de variáveis.
# String
nome = "Michael"
print(f'Nome : {nome} type : {type(nome)})')

 #Inteiro
numero = 1985
print(f'Número : {numero} type : {type(numero)})')
 
# Float
Valor = 150.35
print(f'Valor : {Valor} type : {type(Valor)})')
 
# Boolean
semExiste = True
print(f'Existe : {semExiste} type : {type(semExiste)})')

